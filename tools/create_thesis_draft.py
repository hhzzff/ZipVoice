def set_paragraph_text(paragraph: Paragraph, text: str) -> None:
    for run in paragraph.runs:
        run.text = ""
    if paragraph.runs:
        paragraph.runs[0].text = text
    else:
        paragraph.add_run(text)


def delete_paragraph(paragraph: Paragraph) -> None:
    element = paragraph._element
    element.getparent().remove(element)
    paragraph._p = paragraph._element = None


def delete_block_item(item) -> None:
    element = item._element
    parent = element.getparent()
    if parent is not None:
        parent.remove(element)


def insert_paragraph_before(paragraph: Paragraph, text: str = "", style: str | None = None) -> Paragraph:
    new_p = OxmlElement("w:p")
    paragraph._p.addprevious(new_p)
    new_para = Paragraph(new_p, paragraph._parent)
    if style:
        new_para.style = style
    if text:
        new_para.add_run(text)
    return new_para


def add_page_break_before(paragraph: Paragraph) -> None:
    p = insert_paragraph_before(paragraph)
    p.add_run().add_break(WD_BREAK.PAGE)


def replace_cover_title(doc: Document) -> None:
    for paragraph in doc.paragraphs:
        if paragraph.text.strip() == "本科生综合论文训练标题":
            set_paragraph_text(paragraph, TITLE)
            return


def clear_symbol_examples(doc: Document) -> None:
    start = None
    end = None
    for i, paragraph in enumerate(doc.paragraphs):
        text = paragraph.text.strip()
        if text == "符号和缩略语说明":
            start = i
        elif start is not None and text == "引    言":
            end = i
            break

    if start is None or end is None:
        return

    for paragraph in list(doc.paragraphs[start + 1 : end]):
        if paragraph.text.strip():
            delete_paragraph(paragraph)


